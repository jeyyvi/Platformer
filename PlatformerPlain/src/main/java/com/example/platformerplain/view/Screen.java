package com.example.platformerplain.view;

/**
 * Represents a screen in the game.
 * <p>
 *     This interface defines a method to display the screen.
 *     <br><br>
 *     Implementing classes must provide an implementation for the
 *     {@code show} method to display the screen.
 * </p>
 */
public interface Screen {

    /**
     * Displays the screen.
     */
    void show();
}
